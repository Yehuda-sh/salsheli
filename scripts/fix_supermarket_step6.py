                                'אשכוליו', 'פומלה', 'ליצ\'י',
                                'כרובית טרי', 'כרוב ל', 'כרוב אדום',
                                'כרוב לבן', 'בצל סגול', 'בצל יבש',
                                'תירס בקלח', 'בטטה אדומה', 'בטטה טרי',
